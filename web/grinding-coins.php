    <h3>高效赚金币和零件</h3>
    <p>
      以下是金币和零件的主要来源：
      <ul>
        <li><span class="in-line-blue-bold">杯赛宝箱：</span>每次获得杯赛第一后，只要三个宝箱位置至少有一个空位，就能得到一个宝箱。每个宝箱需要等到3-24小时才能打开。但如果你想尽快获得更多金币和配件，可以用钻石打开。关于如何获得更多钻石，详见本页之后的章节。关于杯赛宝箱的规律，金币零件的数量，详见<a href="/web/cups.php?lang=zh">杯赛总览</a>。另外，每天早上8点到晚上8点，以及晚上8点到次日早上8点，会有6次看广告免费打开3小时宝箱的机会，如果你订阅了VIP，更是可以跳过广告直接打开3小时箱。利用好这些机会可以节省钻石，多开不少箱子。</li>
        <li><span class="in-line-blue-bold">冒险宝箱：</span>系统会定时给你冒险任务。任务会指定一张冒险图，有时会指定一台车，有时则是任何车都可以。如果你跑到至少500米，就算完成了冒险任务，会得到一个冒险宝箱。不过，并不是不管你跑多远，宝箱等级都是一样的。跑得越远，宝箱级别越高。具体详见<a href="/web/adventure.php?lang=zh#adventure_task">冒险任务</a>。获得任务的时间是有规律的：每次开始跑任务开始计算，12小时候会拿到3个新任务。比如你上午9点拿到3个新任务，你开始跑第一个，但刚开始就有事暂停了游戏，一小时后回来跑完了第一个任务。然后又在下午5点下班后跑完了剩余2个。那么到晚上9点，你又会拿到3个新任务。假如你下午忘记跑那2个任务了，到了晚上9点你除了已有的2个任务，还会得到1个新任务，总共还是3个。大多数时间不够的玩家都会选择每个任务跑500米，拿到等级为1的宝箱。当然，如果时间充裕，你可以选择擅长的任务，跑数万米，尤其是在有双倍金币的时候。</li>
        <li><span class="in-line-blue-bold">每周活动宝箱：</span>每周都有一次公开活动，只要达到杯赛银I，就能参加。详见<a href="/web/event.php?lang=zh">每周活动总览</a>。奖励一般有金币，宝箱，皮肤，零件。点<a href="/web/event-past-events.php?lang=zh">过往活动</a>可以查看过去每次活动的内容和奖励。如果你订阅了VIP，那么每场比赛的分数都会翻倍，这样可以更快拿齐所有奖励。</li>
        <li><span class="in-line-blue-bold">团队宝箱和比赛奖励</span>：如果你达到了杯赛金I等级，就应该尽快加入团队。加入团队后，每周都能领取一个距离宝箱(固定在周一上午8点)。距离宝箱的等级算法和所含物品见<a href="/web/team.php?lang=zh#team_chest_zh">这里</a>。此外，每两天会有一场队赛，不管队赛输赢，最后基本都有奖励。奖励内容见<a href="/web/team.php?lang=zh#team_match_prize_zh">这里</a>。哪怕队赛需要用到的车你一辆也没有，也应该参赛（虽然得分为0）。只要参赛了，就有队赛奖励，否则就没有。</li>
        <li><span class="in-line-blue-bold">其他获得金币和零件的机会：</span>每个赛季（其实就是每个自然月），前5万杯赛分有额外的奖励，包括金币，宝箱，零件。如果有VIP或者高级通行证，还能有额外的奖励。所以每个月尽量跑满5万杯赛分。VIP用户每天可以领取一个VIP宝箱。每隔6~8小时，可以在商店里领一个蓝色宝箱。每天跑赢10个赛道，可以得到一个红色宝箱。每天登陆游戏也有一些金币或者宝箱奖励。各种宝箱的来源和金币零件数量，请见<a href="/web/chests-and-parts.php?lang=zh">宝箱与零件</a>。
        </li>
      </ul>
