    <h3>新手上路</h3>
    (by PR-Rou🍏)<br>
    <p>登山赛车2的可玩性还是挺高的。游戏大致可以分为四个模块：冒险，杯赛，团队，活动。新玩家可能一时不知道从哪玩起，也不知道升级车子和配件的先后顺序。本篇就谈一下我的建议。</p>
    <p><span class="in-line-blue-bold">0. 首先说一下为什么要升级。</span>目前游戏中一共有22辆车，每个车都有4个组件，每个组件都分20级，新买的车4个组件都是1级，最高可以升到20级。另外，每个车又有15个左右的配件，配件分4个种类：普通级配件有15级，稀有级配件有10级，史诗级配件有7级，传奇级配件有4级。无论跑什么比赛(冒险，杯赛，队赛)，最多可以带3个配件。不同的级别性能是不同的，第一级和最高级的差距尤其大。对于冒险来说，用低级别车子好不容易跑到的距离，用级别高的车，可能轻松就能跑到。对于杯赛来说，系统会根据你车子的级别匹配接近的对手，但总体来说，高配置的车子还是有优势的。尤其当你使用的车子和配件已经满级，那就能保证对手的车子不会被你更好。这就是为什么杯赛一开始会很难，而越到后面越容易，因为你在不断升级车子的过程中，对手车子的优势逐渐变小。
    </p>
    <p><span class="in-line-blue-bold">1. 升级车子和配件需要用到什么呢？</span>在登山赛车2中，购买车子，升级车子需要金币，而升级配件，不仅需要金币，还需要足够的零件。举例来说，假如目前你登山赛车的起步加速是1级，你想升到2级，除了需要支付4100金币（看广告可以免去支付这4100金币）外，还需要3个登山赛车起步加速零件。如果你要继续升到3级，那么除了需要6800金币，还要10个这样的零件。具体每个配件每个级别需要多少金币和零件，可以看<a href="/web/upgrade-cost.php?lang=zh">升级成本</a>。
    </p>
    <p><a id="grinding_coins_parts_source"></a><span class="in-line-blue-bold">2. 那么从哪里获得金币和零件呢？</span>主要来源就是各种宝箱。常见的宝箱有：杯赛宝箱，冒险任务宝箱，每周活动宝箱，团队距离箱和比赛奖励里的宝箱。另外，商店中也有用金币或者钻石买零件的机会，但大部分情况都不推荐买，原因稍后会解释。关于赚金币和零件的详细情况，见下面的赚金币零件部分。</p>
    <p><span class="in-line-blue-bold">3. 哪些需要升级，哪些不需要？</span>这个游戏好玩的地方就在于，每个车和配件都有各自的特点，针对不同的赛道，目标，需要不同的车和配件。跑杯赛用拉力赛车最好，因为针对各种地形表现都不错，而且还有顶棚可以防止撞头暴毙。热棒飞车也是个不错的选择。跑冒险，虽然拉力也能跑出不错的距离，但要想跑得更远，坦克或者竞速卡车可能是更好的选择，因为有最大的油箱。跑竞速，不少人又使用超级跑车或者柴油车。而队赛里车子和配件的搭配组合就更多了。所以，<span class="in-line-blue-bold">不要报废任何零件</span>，哪怕是你觉得没用的。下面分别说一下购买赛车，升级赛车，和升级配件。
      <ul>
        <li><span class="in-line-blue-bold">购买赛车：</span>刚开始玩的时候，你会发现极度缺乏金币，不知道某个车要不要购买。其实所有的车最终都要买，而且你会很快发现买车花不了多少钱。但前期还是可以考虑优先买这几个车：巴士，沙丘越野车，拉力赛车。详见<a href="/web/cups-tips.php?lang=zh">杯赛新手升级攻略</a>。
        </li>
        <li><span class="in-line-blue-bold">升级赛车：</span>升级赛车需要不少金币，但并不如升级配件需要的那么多。所以能省则省。首先，每隔三小时有一次看广告升级车子组件的机会，不要浪费。每个车可以看广告免费升到第几级，看<a href="/web/upgrade-cost.php?lang=zh#upgrade_cost_vehicles_zh">这个表格</a>。其次，每个车的四个组件不是同样重要的，甚至有些组件级别低更好。总体来说，赛车的引擎和轮胎都是很重要的，需要升级，但每一级提升的性能越来越少，而价格越来越高，所以前期升到比如15级，就暂时可以了。不少车有悬挂可以升级，悬挂比较重要，所以可以考虑升到12级。而一些车的平衡，空翻，护架，下压力等，则可以除了免费升级外，一点都不用花金币升级。
        </li>
        <li><span class="in-line-blue-bold">升级配件：</span>升级配件需要比升级赛车更多的金币，所以一定要精打细算。首先，和升级赛车一样，每隔三小时有一次看广告升级配件的机会，不要浪费。每个配件可以看广告免费升到第几级，看<a href="/web/upgrade-cost.php?lang=zh#upgrade_cost_parts_zh">这个表格</a>。其次，同样的，不是每个配件同样重要，有些配件极少甚至完全用不到。总体来说，翅膀和金币加速最为重要，其次是磁铁，冬胎，起步加速，涡轮增压（过冲涡轮），超频引擎（后燃）。针对不同赛车，某些配件可能特别有用，比如巴士的推进器。
        </li>
      </ul>
    </p>
