<h3>冒险 - 森林 (Forest)</h3>
<p>（感谢CN-Touren提供无比详细的攻略！）</p>
<p>森林10000米内主要难点分布：
  <ul>
    <li>3600不大不小的坡，个别车需要注意速度，如：MK、怪卡、巴士；</li>
    <li>4850（475下坡就要减速，飞过就没可能再吃到油）、5450和5750两个连续油点在飞跃之后的下坡（不小心也还好，基本上够时间退回去吃油）</li>
    <li>6800油在悬木下方（最难油点），过法：1.带铁架的车可以直接撞开悬木吃油；2.带不了铁架的情况，可以先开到悬木上通过微操调整重心控制悬木旋转吃油，需要前面够快在这里留有足够的油调整；3.减速调整车头从缝隙处直接撞开悬木，但极易暴毙不推荐，只适用于到这里确实没有油了碰碰运气。</li>
    <li>4600、6400、7600，三处有深潭，其中前两处都是在下坡之后，所以下坡尽量贴坡保持速度飞过去，带翅膀切忌在坡顶猛向前飞，7600飞跃前有个小凹陷可以调整好姿态加速飞问题不大；</li>
    <li>7300是万米前最大的坡，对小绵羊、巴士、MK、美摩、怪卡、羊癫疯这几辆车是个不小的挑战，前面分别已经说明过法；</li>
    <li>8000之后有很多连续凹凸不平的路面，找好猛冲和落地的点（特别是超跑、超摩、方程式等），不要按死油门不松手磕磕碰碰耽误时间；</li>
    <li>建议：先用拉力、坦克、竟卡、柴油、沙滩车等简单的车跑过，多留意并记住特殊油点和路线，其他车跑起来也不至于同一个点多次暴毙；</li>
  </ul>
</p>
